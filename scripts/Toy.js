class Toy{

    // Constructor for a toy class
    constructor(name,price,rating,quantity,imageLink,otherLink,toyType){
        this.name=name;
        this.price=price;
        this.rating=rating;
        this.quantity=quantity;
        this.imageLink=imageLink;
        this.otherLink=otherLink;
        this.toyType=toyType;
    }


}